'use client'

import Image from 'next/image'
import { useState } from 'react'
import { formatPrice, timeAgo } from '@/lib/utils'
import { LikeButton } from './LikeButton'
import type { ListingWithProfile } from '@/types/database'

interface StoryCardProps {
    listing: ListingWithProfile
    userId?: string
}

const categoryIcons: Record<string, string> = {
    roupas: 'üëï',
    eletronicos: 'üì±',
    moveis: 'üõãÔ∏è',
    eletrodomesticos: 'üîå',
    brinquedos: 'üß∏',
    esportes: '‚öΩ',
    veiculos: 'üöó',
    outros: 'üì¶',
}

export function StoryCard({ listing, userId }: StoryCardProps) {
    const [showWhatsAppPrompt, setShowWhatsAppPrompt] = useState(false)

    const handleWhatsAppClick = async () => {
        // Track analytics
        try {
            await fetch('/api/analytics', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({
                    listingId: listing.id,
                    action: 'whatsapp_click',
                }),
            })
        } catch (error) {
            console.error('Analytics error:', error)
        }

        // Open WhatsApp
        if (listing.profiles.phone) {
            const message = `Ol√°! Vi seu an√∫ncio "${listing.title}" no Dezapeg√£o e tenho interesse!`
            const whatsappUrl = `https://wa.me/${listing.profiles.phone.replace(/\D/g, '')}?text=${encodeURIComponent(message)}`
            window.open(whatsappUrl, '_blank')
        } else {
            setShowWhatsAppPrompt(true)
        }
    }

    return (
        <article className="bg-white rounded-2xl shadow-lg overflow-hidden hover:shadow-xl transition-shadow">
            {/* Image */}
            <div className="relative aspect-[4/5] max-h-[600px] bg-gray-100">
                <Image
                    src={listing.image_url}
                    alt={listing.title}
                    fill
                    className="object-cover"
                    sizes="(max-width: 768px) 100vw, (max-width: 1200px) 50vw, 33vw"
                />

                {/* Category badge */}
                <div className="absolute top-4 left-4 bg-white/90 backdrop-blur-sm px-3 py-1 rounded-full text-sm font-medium flex items-center gap-1">
                    <span>{categoryIcons[listing.category]}</span>
                    <span className="capitalize">{listing.category}</span>
                </div>

                {/* Timestamp */}
                <div className="absolute top-4 right-4 bg-black/50 backdrop-blur-sm px-3 py-1 rounded-full text-white text-xs">
                    {timeAgo(listing.created_at)}
                </div>
            </div>

            {/* Content */}
            <div className="p-5">
                {/* Title and Price */}
                <div className="mb-3">
                    <h2 className="text-xl font-bold text-gray-900 mb-1">{listing.title}</h2>
                    <p className="text-2xl font-bold text-purple-600">{formatPrice(listing.price)}</p>
                </div>

                {/* Description */}
                {listing.description && (
                    <p className="text-gray-600 text-sm mb-4 line-clamp-2">{listing.description}</p>
                )}

                {/* Seller */}
                <div className="flex items-center mb-4">
                    <div className="w-8 h-8 bg-gradient-to-br from-purple-600 to-pink-500 rounded-full flex items-center justify-center text-white font-semibold text-sm">
                        {listing.profiles.username[0].toUpperCase()}
                    </div>
                    <span className="ml-2 text-sm text-gray-700 font-medium">
                        {listing.profiles.username}
                    </span>
                </div>

                {/* Actions */}
                <div className="flex items-center space-x-3">
                    <LikeButton
                        listingId={listing.id}
                        initialLikes={listing.likes_count}
                        userId={userId}
                    />

                    <button
                        onClick={handleWhatsAppClick}
                        className="flex-1 bg-green-500 hover:bg-green-600 text-white py-3 rounded-xl font-semibold transition flex items-center justify-center space-x-2"
                    >
                        <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 24 24">
                            <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
                        </svg>
                        <span>WhatsApp</span>
                    </button>
                </div>

                {showWhatsAppPrompt && (
                    <div className="mt-3 p-3 bg-yellow-50 border border-yellow-200 rounded-lg text-sm text-yellow-800">
                        Vendedor n√£o cadastrou WhatsApp. Tente outro an√∫ncio.
                    </div>
                )}
            </div>
        </article>
    )
}
